     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $sectionData = Section::all();
        
        if (auth()->user()->userType == 2) {

           $auth_id = auth()->id();
        }
        else{

            $auth_id = auth()->user()->instructor->institute_id;
        }


         if (auth()->user()->userType !=1) {
            $section_id = [];
              foreach ($sectionData as $key => $section_value) 
              {
                if ($section_value->institute_id != $auth_id) 
                {
                    unset($sectionData[$key]);
                }   
                else {
                  $section_id[] = $section_value->id;
              } 
            }
         }
        return view('section.index',compact('sectionData'));
    }

