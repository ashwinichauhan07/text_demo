    {
        
        $data = request()->validate([
            'name'=>'required',
        ]);

        // dd(auth()->id());

        if (auth()->user()->userType == 2) {

           $auth_id = auth()->id();
        }
        else{

            $auth_id = auth()->user()->instructor->institute_id;
        }


       $data = Language::create([
